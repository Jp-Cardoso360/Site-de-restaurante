var listeNumber = [3,4,5,9,4,67,8,7,89];
var listRoub = listeNumber.map(function(element){
  return element*3.
})
var filt = listRoub.filter(function(element){
  return element >= 25;
})
var soma = filt.reduce(function(prov,curr){
  return prov + curr;
})
console.log(filt)
console.log('Soma total ',soma) 
